function populate() {

    for(i = 0; i < gamesList.length; i++){    

    const gameName = gamesList[i].name;
    const gameTime = gamesList[i].hours_forever;
    const gameLogo = gamesList[i].logo;
    
    var table = document.getElementById('gameTable');
    var row = table.insertRow();
    var cell1 = row.insertCell(0);
    var cell2 = row.insertCell(1);
    var cell3 = row.insertCell(2);

    cell1.innerHTML = gameName;

    if(gameTime !== undefined){
        cell2.innerHTML = gameTime;
        }
    else {
        cell2.innerHTML = 'Not played';
        }
    cell3.innerHTML = `<img src = ${ gameLogo }>`;
    }
};

function pickMeOne() {
    let unplayedGames = [];

    for (i = 0; i < gamesList.length; i++){
        const gameName = gamesList[i].name;
        const gameTime = gamesList[i].hours_forever;
    
    if(typeof gameTime === "undefined"){
        unplayedGames.push(gameName);
        }
    }
    var gameToRecommend = unplayedGames[Math.floor(Math.random() * unplayedGames.length)];
    var randomBox = document.getElementById('randombox');
    randomBox.innerHTML = gameToRecommend;
};